    TOE0 |= _0001_TAU_CH0_OUTPUT_ENABLE;
    NFEN1 |= _01_TAU_CH0_NOISE_ON;    /* enable using noise filter of TI00 pin input signal */
    /* Channel 1 is used to measure input pulse low-/high-width */
    TIS0 |= _00_TAU_CH1_INPUT_TI01;
    TMR01 = _8000_TAU_CLOCK_SELECT_CKM1 | _0000_TAU_CLOCK_MODE_CKS | _0000_TAU_16BITS_MODE |
            _0200_TAU_TRIGGER_TIMN_BOTH | _00C0_TAU_TIMN_EDGE_BOTH_HIGH | _000C_TAU_MODE_HIGHLOW_MEASURE;
    TOM0 &= ~_0002_TAU_CH1_OUTPUT_COMBIN;
    TOL0 &= ~_0002_TAU_CH1_OUTPUT_LEVEL_L;
    TO0 &= ~_0002_TAU_CH1_OUTPUT_VALUE_1;
    TOE0 &= ~_0002_TAU_CH1_OUTPUT_ENABLE;
    NFEN1 |= _02_TAU_CH1_NOISE_ON;    /* enable using noise filter of TI01 pin input signal */
    /* Channel 2 is used to measure input pulse low-/high-width */
    TMR02 = _8000_TAU_CLOCK_SELECT_CKM1 | _0000_TAU_CLOCK_MODE_CKS | _0000_TAU_COMBINATION_SLAVE |
            _0200_TAU_TRIGGER_TIMN_BOTH | _0080_TAU_TIMN_EDGE_BOTH_LOW | _000C_TAU_MODE_HIGHLOW_MEASURE;
